package vo;

public class PayInfo {

	private String sel_num;
	private String pay_date;
	private String pay_type;
	private String acc_name;
	private String acc_bank;
	private String pay_exp;
	
	public String getSel_num() {
		return sel_num;
	}
	public void setSel_num(String sel_num) {
		this.sel_num = sel_num;
	}
	public String getPay_date() {
		return pay_date;
	}
	public void setPay_date(String pay_date) {
		this.pay_date = pay_date;
	}
	public String getPay_type() {
		return pay_type;
	}
	public void setPay_type(String pay_type) {
		this.pay_type = pay_type;
	}
	public String getAcc_name() {
		return acc_name;
	}
	public void setAcc_name(String acc_name) {
		this.acc_name = acc_name;
	}
	public String getAcc_bank() {
		return acc_bank;
	}
	public void setAcc_bank(String acc_bank) {
		this.acc_bank = acc_bank;
	}
	public String getPay_exp() {
		return pay_exp;
	}
	public void setPay_exp(String pay_exp) {
		this.pay_exp = pay_exp;
	}
}
